-- Creacion de Vistas
CREATE VIEW PCLUB.VIEW_ADMPT_KARDEX AS
  SELECT A.ADMPN_ID_KARDEX,
        A.ADMPN_COD_CLI_IB,
        A.ADMPV_COD_CLI,
        A.ADMPV_COD_CPTO,
        A.ADMPD_FEC_TRANS,
        A.ADMPN_PUNTOS,
        A.ADMPV_NOM_ARCH,
        A.ADMPC_TPO_OPER,
        A.ADMPC_TPO_PUNTO,
        A.ADMPN_SLD_PUNTO,
        A.ADMPC_ESTADO,
        A.ADMPV_IDTRANSLOY,
        A.ADMPD_FEC_REG,
        A.ADMPD_FEC_MOD,
        A.ADMPV_DESC_PROM,
        A.ADMPN_TIP_PREMIO,
        A.ADMPD_FEC_VCMTO,
        A.ADMPV_USU_REG,
        A.ADMPV_USU_MOD
   FROM ADMPT_KARDEX A 
   UNION ALL
     SELECT B.ADMPN_ID_KARDEX,
        B.ADMPN_COD_CLI_IB,
        B.ADMPV_COD_CLI,
        B.ADMPV_COD_CPTO,
        B.ADMPD_FEC_TRANS,
        B.ADMPN_PUNTOS,
        B.ADMPV_NOM_ARCH,
        B.ADMPC_TPO_OPER,
        B.ADMPC_TPO_PUNTO,
        B.ADMPN_SLD_PUNTO,
        B.ADMPC_ESTADO,
        B.ADMPV_IDTRANSLOY,
        B.ADMPD_FEC_REG,
        B.ADMPD_FEC_MOD,
        B.ADMPV_DESC_PROM,
        B.ADMPN_TIP_PREMIO,
        B.ADMPD_FEC_VCMTO,
        B.ADMPV_USU_REG,
        B.ADMPV_USU_MOD
   FROM ADMPT_KARDEX_MIG B;